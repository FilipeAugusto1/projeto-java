public class Interruptores {
    public static void main(String[] args) {
        System.out.println("1. Ligue o primeiro interruptor e deixe-o ligado por alguns minutos.");
        System.out.println("2. Desligue o primeiro interruptor e ligue o segundo imediatamente.");
        System.out.println("3. Vá até a sala onde estão as lâmpadas:");
        System.out.println("   - A lâmpada que está acesa corresponde ao segundo interruptor.");
        System.out.println("   - A lâmpada que está apagada mas quente ao toque corresponde ao primeiro interruptor.");
        System.out.println("   - A lâmpada que está apagada e fria ao toque corresponde ao terceiro interruptor.");
    }
}